import { createBrowserRouter, RouterProvider } from "react-router-dom"
import LandingPage from "./pages/Landing";
import Dashboard from "./pages/Dashboard";




function App() {
  const router = createBrowserRouter([
    {
      path: "/",
      element: <LandingPage />,
    } ,
    {
      path: "/dashboard",
      element: <Dashboard />,
    }
  ]);

  return (
    <>
      <RouterProvider router={router} />
    </>
  )
}

export default App
